public class AutoboxingAndUnboxing {
    public static void main(String[] args) {
        //Autoboxing
        Integer i = new Integer(200);
        int i1=i;
        System.out.println(i);
        System.out.println(i1);
        //Unboxing
        int i2=10;
        Integer i3=i2;
        System.out.println(i2);
        System.out.println(i3);
    }

}
